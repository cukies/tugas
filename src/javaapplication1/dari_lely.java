    private static double fungsi(double x){        
        
        //f = Math.pow(x, 3) + Math.cos(x);
        f = Math.pow(x, 3) + Math.pow(x, 2) - (3*x) - 3;
        
        return f;
    }
    
    private static double fungsiTurunan(double x){
        f = (3*Math.pow(x, 2)) + (2*x) - 3;
        //f = (3*Math.pow(x, 2)) - Math.sin(x);
        return f;
    }
    
    private static double cariB(){
        b = a - (fungsi(a)/fungsiTurunan(a));
        return b;
    }

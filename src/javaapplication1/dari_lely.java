    public void jalan(){
        double tampilA, tampilB, cari, aa=0;
        String hasilA;
        int i = 1;
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();      
        tampilB = fungsi(cariB());
        while(tampilB >= e) {
            Object[] o = new Object[5];                                  
            tampilA = fungsi(a);
            fungsiTurunan(a);
            cari = cariB();
            tampilB = fungsi(cari);
            o[0] = i;
            o[1] = desimal.format(a); 
            o[2] = desimal.format(cari);
            o[3] = desimal.format(tampilA);
            o[4] = desimal.format(tampilB);
            aa = a;
            model.addRow(o);
            a = b;
            i++;
        }
        salah = (b-aa)/b;
    }

